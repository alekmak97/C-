void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) // 0 - ссылка на количество строк
    {
        for (int j = 0; j < image.GetLength(1); j++) // 1 - ссылка на количество столбцов
        {
            // Console.Write($"{image[i, j]} ");
            if(image[i,j] == 0) Console.Write($" ");
            else Console.Write($"+");
        }
    Console.WriteLine();
    }
}

