// Закраска картинк внутри объекта, только для замкнутого объекта
void FillImage(int row, int col)
{
    if (pic[row,col] == 0)
    {
        pic[row,col] = 1;
        // Правила хождения и закраски объекта внутри какой блок за чем идёт
        FillImage(row-1,col);
        FillImage(row,col-1);
        FillImage(row+1,col);
        FillImage(row,col+1);

    }
}

